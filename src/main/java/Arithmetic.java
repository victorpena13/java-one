// the artihmetic class is only meant to be referenced statically. meaning we never create instances of it.
public class Arithmetic {

    // static property:
    public static double pi = 3.13159;

    // static methods:
    public static int add(int x, int y) {
        return x + y;
    }

    public static int multiply(int x, int y) {
        return x + y;
    }

}
