public class StringExercise {

    public static void main(String[] args) {

        String anotherBrickInTheWall = "We don't need no education\nWe don't need no thought control";
        System.out.println(anotherBrickInTheWall);
        String quotationsInsideQuotations = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(quotationsInsideQuotations);
        String backslash = "In windows, the main drive is usually C:\\";
        System.out.println(backslash);
        String backslashes = "I can do backslashes \\, double backslashes \\, and the amazing triple backslash \\\\\\!";
        System.out.println(backslashes);
    }
}
